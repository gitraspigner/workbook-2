package com.pluralsight.LoopingExercises;

/**
 * Displays the text "I love Java" 5 times to the user, using a while loop.
 *
 * @author Ravi Spigner
 */
public class WhileLoop {
    public static void main(String[] args) {
        int i = 0;
        while (i < 5) {
            System.out.println("I love Java");
            i++;
        }
    }
}
